const nodemailer = require("nodemailer");
const env = require("../config/env");

module.exports.sendEmail = async (
  email,
  verificationLink = null,
  passwordResetCode = null
) => {
  const transporter = nodemailer.createTransport({
    service: "gmail",
    auth: {
      user: env.emailUser,
      pass: env.emailPass,
    },
  });

  const mailOptions = {
    from: env.emailUser,
    to: email,
    subject: verificationLink ? "Verify Your Account" : "Password Reset Code",
    html: verifactionEmailTemplate(verificationLink, passwordResetCode),
  };

  await transporter.sendMail(mailOptions);
};

